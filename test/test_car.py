import unittest
from datetime import datetime
from carriganTires import CarriganTires
from octoprimeTires import OctoprimeTires


from carFactory import CarFactory


class TestCalliope(unittest.TestCase):
    def setUp(self):
        self.carFactory = CarFactory()


    def test_battery_should_be_serviced(self):
        today = datetime.today().date()
        last_service_date = today.replace(year=today.year - 3)
        current_mileage = 0
        last_service_mileage = 0

        car = self.carFactory.create_calliope(today, last_service_date, current_mileage, last_service_mileage)
        self.assertTrue(car.needs_service())

    def test_battery_should_not_be_serviced(self):
        today = datetime.today().date()
        last_service_date = today.replace(year=today.year - 1)
        current_mileage = 0
        last_service_mileage = 0

        car = self.carFactory.create_calliope(today, last_service_date, current_mileage, last_service_mileage)
        self.assertFalse(car.needs_service())

    def test_engine_should_be_serviced(self):
        last_service_date = datetime.today().date()
        current_mileage = 30001
        last_service_mileage = 0

        car = self.carFactory.create_calliope(last_service_date, last_service_date, current_mileage, last_service_mileage)
        self.assertTrue(car.needs_service())

    def test_engine_should_not_be_serviced(self):
        last_service_date = datetime.today().date()
        current_mileage = 30000
        last_service_mileage = 10

        car = self.carFactory.create_calliope(last_service_date, last_service_date, current_mileage, last_service_mileage)
        self.assertFalse(car.needs_service())


